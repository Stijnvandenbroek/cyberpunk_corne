static void render_status(void) {
    switch (get_highest_layer(layer_state)) {
        case _BASE:
			render_nothing();
            break;
        case _LOWER:
			render_nothing();
            break;
        case _RAISE:
			render_nothing();
            break;
        case _GAMING:
            render_gaming();
            break;
        case _GAMINGRAISE:
            render_gamingraise();
            break;
        default:
            oled_write_P(PSTR("Undefined"), false);
    }
}


